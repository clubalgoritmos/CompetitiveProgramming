#  Descripción
#    Se desea implementar un sistema que permita almacenar y buscar información de contactos de una agenda telefónica. Cada contacto debe contener un nombre (cadena de caracteres) y un número telefónico (entero). El sistema debe permitir agregar, buscar y eliminar contactos de la agenda.
#   Entrada
#    La entrada consistirá en varias líneas de texto, donde cada línea representa una operación a realizar en la agenda. Las operaciones pueden ser:Agregar contacto: la línea contendrá la palabra "add" seguida del nombre y número telefónico separados por un espacio.Buscar contacto: la línea contendrá la palabra "find" seguida del nombre del contacto a buscar.Eliminar contacto: la línea contendrá la palabra "del" seguida del nombre del contacto a eliminar.
#   Salida
#    La salida debe ser una línea por cada operación de búsqueda, que contenga el resultado de la búsqueda o la confirmación de eliminación del contacto.
#   Ejemplo Entrada
#    add Ana 1234567890add Pedro 9876543210find Anadel Pedrofind Pedro
#   Ejemplo Salida
#    1234567890Contacto eliminado: PedroContacto no encontrado: Pedro
