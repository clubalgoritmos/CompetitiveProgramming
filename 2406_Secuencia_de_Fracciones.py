#   Entrada
#    La entrada comienza con un numero que representa el número de casos de prueba. Luego sigue un número $n$ ($0 \leq n \leq 18$) por cada casos de prueba.
#   Salida
#    Por cada caso de prueba imprima la fracción resultante de la secuencia $f_n$ como se muestra en el ejemplo.
#   Ejemplo Entrada
#    40123
#   Ejemplo Salida
#    11/22/37/10
