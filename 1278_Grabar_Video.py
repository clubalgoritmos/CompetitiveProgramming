#  Descripción
#    Juan es un gran fanático de la serie de los Simpsons. Para ver todos los programas se ha comprado una antena satelital. Desea programar su grabadora de vídeo.
#    No es fácil, el video puede grabar solamente un canal a la vez. Por otra parte, los episodios pueden tener diferentes longitudes (dependiendo de la forma en que se editan, los anuncios, etc.).
#    ¿Puede ayudar a Juan? Escriba un programa que, dada la hora de inicio y hora de finalización de la emisión de todos los episodios de los Simpsons en todos los canales de varios días, calcule el número máximo de episodios que se pueden grabar cada día.
#    Versión original:https://www.jutge.org/problems/P40128_en
#   Entrada
#    La entrada consiste en varios casos. Cada caso tiene un número natural 1<=n<=105 seguido de n pares (I1, F1) . . .  (In, Fn) de números naturales que indican la hora de inicio y el fin de la transmisión, ambos incluidos, de cada episodio de un día. Para cualquier j entre 1 y n, asuma 0 <=Ij <= Fj<=107.
#   Salida
#    Para cada caso de entrada, imprimir una línea con el número máximo de episodios completos que Juan será capaz de grabar ese día.
