#    Andres, Sergio y Miguel son amantes de los dulces, sin embargo, sus preferencias por los dulces son diferentes. Para hacerlos felices a todos, debería ocurrir lo siguiente:
#    Andres, Sergio y Miguel deben comer al menos x, y,z dulces, respectivamente.
#    Andres tiene una afinidad extrema por los dulces verdes, por lo que solo comerá dulces verdes y solo dulces verdes.
#    Por otro lado, Sergio no es un fanático de los dulces negros; cualquier tipo de dulce, excepto los dulces negros, le serviría. En otras palabras, Sergio puede comer dulces verdes y moradas.
#    Miguel tiene un gusto común: disfruta de los dulces en general y estará satisfecho con cualquier tipo de dulce, siempre que la cantidad sea suficiente.
#    Sabiendo que a sus amigos les gustan tanto los dulces, Valeria decidió organizar una fiesta de dulces con ellos. Ha preparado una caja con a dulces verdes, b dulces morados y c dulces negros. Sin embargo, Valeria no está segura de si la caja que preparó contiene suficientes dulces para hacer felices a todos. ¿Puede averiguar si es posible distribuir dulces para que todos estén contentos o si Valeria tiene que comprar más dulces?
#    No es necesario distribuir todos los dulces, por lo que es posible que algunos de ellos se queden sin usar.
#   Entrada
#    La primera línea contiene tres números enteros x, y, z (1≤x, y, z≤105 ): el número de dulces que Andres, Sergio y Miguel quieren comer. La segunda línea contiene tres números enteros a, b, c (1≤a, b, c≤105 ) - el número de dulces verdes, morados y negros en la caja.
#   Salida
#    Si hay una distribución de dulces que permite que todos estén contentos, escriba "SI", de lo contrario, escriba "NO".
