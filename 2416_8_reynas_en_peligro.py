#  Descripción
#    El problema de las ocho reinas consiste en poner ocho reinas en el tablero de ajedrez sin que se amenacen. En el juego del ajedrez la reina amenaza a aquellas piezas que se encuentren en su misma fila, columna o diagonal. El juego de las 8 reinas consiste en poner sobre un tablero de ajedrez ocho reinas sin que estas se amenacen entre ellas.Ahora vino un malvado dedicándose a la tarea de cavar pozos profundos en algunas casillas del tablero. Por lo tanto no es posible colocar una reina en es lugar. El tablero de ajedrez tiene 8 filas y 8 columnas y las reinas se comienzan a acomodar desde la posición 1,1 del tablero de ajedrez.En el problema te darán cuatro lugares donde hay pozos profundos. Tu tarea es contar de cuantas maneras se pueden acomodar las ocho reinas sin que se amenace y ninguna este en hueco profundo.
#   Entrada
#    La entrada consiste de múltiples casos de prueba. La primera línea tiene el número de casos de prueba. Cada caso de prueba consiste de cuatro coordenadas (x,y) que indica donde hay pozos
#   Salida
#    Por cada caso de entrada escriba el número de posibles configuraciones donde se pueden colocar reinas sin caer en un pozo.
#   Ejemplo Entrada
#    31 4 2 5 4 2 8 41 4 1 4 1 4 1 41 4 2 5 6 4 7 4
#   Ejemplo Salida
#    487448
