#  Secuencia Collatz
#  Enviar
#  Estado
#  Descripción
#    La secuencia de Collatz (S) que comienza en un número entero n, se define mediante la fórmula:S (n) = (n / 2) si n es par, de lo contrario ( 3*n + 1)). La secuencia para n es entonces: S (n), S (S (n)), ... hasta que el valor de n llega a 1.En caso de llegar al numero -1 también termina.Por ejemplo, Empezando en n = 6, la sucesión tiene 8 pasos, y llega a la siguiente sucesión: 6, 3, 10, 5, 16, 8, 4, 2, 1.Empezando en n = 11, la sucesión tiene 14 pasos: 11, 34, 17, 52, 26, 13, 40, 20, 10, 5, 16, 8, 4, 2, 1
#   Entrada
#    La entrada consiste en múltiples casos de prueba. La primera linea indica cuantos casos de prueba existen. Cada caso de prueba consiste en una sola línea que contiene un entero decimal positivo$-10^6 \leq n \leq 10^6$, en que comienza la secuencia.
#   Salida
#    Por cada caso de prueba la salida consta de una sola línea que con el número de pasos requeridos para llegar a n. Si se genera un bucle infinito escriba en la salida -1.
#   Ejemplo Entrada
#    8261127123456-7-11
#   Ejemplo Salida
#    181411161-123
