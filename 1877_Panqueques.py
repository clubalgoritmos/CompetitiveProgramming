#  Descripción
#    Hay dos pilas cada una con una cantidad igual de panqueques. Cada panqueque tiene una cierta área. Se quiere sacar la máxima cantidad de panqueques de cada pila de tal manera que la suma de los áreas de los panqueques extraidos de cada pila sean iguales. Es decir se debe sacar una cantidad de panqueques del tope de la primera pila y otra cantidad de panqueques del tope de la segunda tal que la suma de las áreas totales por cada pila respectivamente sean iguales. Si no hay forma de realizar esto el resultado es 0.
#   Entrada
#    La primera línea contiene un número N que representa la cantidad de elementos en cada uno de las pilas. La segunda línea contiene N números que representan el área de los panqueques en el orden que se añadieron a la primera pila. La tercera y última línea contiene N números que representan los panqueques en el orden que se añadieron a la segunda pila.
#    Límites
#    1 <= N <= 10000
#    0 <= A[i], B[i] <= 100, para 1 <= i <= N
#   Salida
#    Imprimir la suma de los áreas total máximo posible como se explica en el enunciado.
#   Ejemplo Entrada
#    61 2 2 3 2 11 3 3 2 3 5
#   Ejemplo Salida
#    10
