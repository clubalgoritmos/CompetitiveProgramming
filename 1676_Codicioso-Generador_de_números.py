#    Dado un numero entero positivo X y un numero entero positivo S. La tarea es encontrar el más pequeño y el más grande de los números que tienen la longitud X y la suma de los dígitos S. Los números requeridos deben ser enteros positivos.
#   Entrada
#    La entrada consiste en un entero T (T<=100) que indica los casos de prueba, cada caso de prueba consiste un par de enteros X, S (1 ≤ X ≤ 100, 0 ≤ S ≤ 900) la longitud y la suma de los dígitos de los números requeridos.
#   Salida
#    Para cada caso de prueba, imprima el par de los números enteros requeridos.primero el número mínimo posible y luego el número máximo posible. Si no existen números que cumplan con las condiciones requeridas, imprima el par de números "-1 -1" (sin las comillas).
