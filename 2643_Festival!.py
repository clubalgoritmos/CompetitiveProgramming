## https://jv.umsa.bo/oj/problem.php?id=2643
#    Copiado al portapapeles
#  Festival!
#  Enviar
#  Estado
#  Descripción
#    La carrera de informática celebra un festival que dura N días. En $M$ de estos días, específicamente en los días $A_1$-ésimo, $A_2$-ésimo, ...,$ A_M$-ésimo, se lanzarán fuegos artificiales. Se garantiza que también habrá fuegos artificiales en el último día del festival (es decir, $A_M = N$ está garantizado).Para cada $i = 1, 2, ..., N$, resuelve el siguiente problema.¿Cuántos días pasan desde el i-ésimo día hasta la próxima ocasión en la que se lancen fuegos artificiales, ya sea en el i-ésimo día o en cualquier día posterior? Si se lanzan fuegos artificiales en el i-ésimo día, se considera que han pasado 0 días hasta la próxima ocasión.
#   Entrada
#    La  primera línea consistirá en 2 valores $N$ y $M$ $(1 \leq M \leq N \leq 2 \times 10^5)$La segunda línea consistirá en los $m$ días $(1 \leq A_1 < A_2 < \ldots < A_M = N)$
#   Salida
#    Imprimir $N$ líneas.La $i$-ésima línea $(1 \leq i \leq N)$ debe contener un número entero que represente la cantidad de días desde el $i$-ésimo día hasta que se lancen los fuegos artificiales por primera vez en o después del $i$-ésimo día.
#   Ejemplo Entrada
#    3 22 3
#   Ejemplo Salida
#    100
#   Ayuda
#    Ejemplo entrada 2
#    8 51 3 4 7 8
#    Ejemplo salida 2
#    01002100

