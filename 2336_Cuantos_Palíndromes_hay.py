#  Cuantos Palíndromes hay?
#  Enviar
#  Estado
#  Descripción
#    Se dice que un número es palíndrome cuando se puede leer igual de izquierda y de derecha. Por ejemplo el número 121 es palíndromo, 100001 es palíndromo, el 1237702 no lo es.
#   Entrada
#    La entrada consiste en varios casos de prueba. La primera línea contiene el número $n$ que indica el número de casos de prueba. Luego siguen $n$ líneas cada una con un número $1\leq k \leq 10,000,000$.
#   Salida
#    En la salida escriba el numero $k$ seguido de la cantidad de números palíndromes que hay hasta $k$ inclusive.
#   Ejemplo Entrada
#    35616003526
#   Ejemplo Salida
#    56 141600 1143526 133
