#    Estas en el colegio y recién llego un compañero de intercambio, parece que aun no domina el idioma pero es muy bueno tomando apuntes.Hoy estuviste muy distraído y no pudiste copiar todo lo que necesitabas, así que le pides prestado su cuaderno, que gran sorpresa darte cuenta que el no escribe con espacios.Bueno, lo poco que alcanzaste a copiar fue el titulo del tema que están avanzando, así que te dispones a buscarlo pero es muy confuso.Escribe un programa que dados los apuntes de tu compañero y el titulo que tu anotaste, puedas saber a partir de que numero de letra empieza los apuntes que necesitas.
