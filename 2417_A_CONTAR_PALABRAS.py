#    A CONTAR PALABRAS
#    Dos palabras P1 y P2 pueden ser generadas a partir de una misma palabra P, siempre y cuando las 3 palabras tengan la misma longitud L. La manera de generar las palabras P1 y P2 a partir de P, es empezar desde una posición K (K<L) y avanzar hasta el final de la palabra, y continuar desde el principio de la palabra.
#    Por ejemplo, la palabra P1=oliviab y la palabra P2=iviabol son generadas a partir de la palabra P=bolivia, P1 con K=2 y P2 con K=4.
#    A partir de una lista de palabras ¿Cuántas de ellas fueron generadas a partir de la primera palabra?
#   Entrada
#    Se tiene un entero N (1 ≤ N ≤ 100) que indica el número de casos, cada línea contiene una palabra P y varias palabras siguientes separadas por un espacio y todo en minúsculas.
#   Salida
#    Imprime en una simple línea un entero, que representa el número de palabras generadas a partir de la primera palabra.
#   Ejemplo Entrada
#    3bolivia oliviab popeye iviabol iviadoumsa saum msau umsa upea peauicpc ccpi pcci cpcpi
#   Ejemplo Salida
#    230
