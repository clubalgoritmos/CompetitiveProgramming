#  Descripción
#    En este problema una flecha a la izquierda se define como el carácter $<$ seguido inmediatamente de cero o más caracteres $-$. Un flecha doble a la izquierda se define como un carácter $<$ seguido de cero o más caracteres consecutivos $= $. Una flecha a la derecha se define como cero o más caracteres $-$ seguidos del carácter $>$. Un flecha doble a la derecha se define como cero o más caracteres $=$ seguidos del carácter $>$.Dada una cadena se quiere hallar la longitud de la flecha más larga.
#   Entrada
#    La entrada contiene varios casos de prueba. Cada caso de prueba es una cadena en una línea. La cadena contiene entre 1 y 50 caracteres. Cada carácter será $<,>,-,=$.La entrada termina cuando no hay más datos.
#   Salida
#    Por cada caso de prueba escriba en una línea la longitud de la cadena más larga. Si no existe una cadena escriba $-1$.
#   Ejemplo Entrada
#    <--->--==><<<<<<<<<<----==-<----=====>
#   Ejemplo Salida
#    41-16
