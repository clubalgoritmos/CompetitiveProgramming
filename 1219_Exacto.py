#  Descripción
#    Exacto es un juego en cual un grupo de cinco amigos se reúnen y ponen sus monedas en el suelo, el ganador del juego se lleva todas las monedas.El juego inicia con que cada amigo diga un numero cualquiera, cada valor de las monedas se resta entre todos los otros valores de las demás (menos del mismo) y se anotan las que sean igual al numero que alguno de los amigos haya mencionado, quien o quienes tengan la mayor cantidad de restas exactas ganan.
#   Entrada
#    La entrada consiste de múltiples casos de prueba. La primera linea indica el numero de casos deprueba. Cada caso de prueba consiste en un numero n (1 <= n <= 100) la cantidad de monedas delos cinco jugadores, luego n números a i siguen (1 ≤ a i ≤ 1000), finalmente cinco números sondados a, b, c, d y e (1 ≤a,b,c,d,e≤ 1000) el numero de cada amigo que se usara para compararlas restas.
#   Salida
#    Imprimir que amigo o logro ganar si hay empate imprimir todos en orden, también imprimirla cantidad máxima de restos iguales por las que se gano y los dos separados por un “ : ”. Alterminar un caso de prueba imprimir una linea en blanco.
