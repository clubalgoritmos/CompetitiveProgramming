#    Es bien conocido que en un acuario algunos peces se pueden comer a otros. Usted tiene un acuario que contiene un cantidad de peces del cual conoce el tamaño. Usted sabe que un pez se puede comer a otro, solo cuando está en el rango de: el doble de tamaño o $10$ veces más grande.Se quiere agregar un pez a la pecera, pero queremos determinar el tamaño para no causar conflictos de comerse con otros peces.Considerando esto usted debe escoger un pez que esté entre los siguientes tamaños
#    Por ejemplo si los tamaños de los peces están entre $1$ y $12$ y queremos insertar un pez, ese puede tener tres posibles tamaños. Los posibles tamaños para el pez que están fuera del rango establecido son $1,11,12$.
#   Entrada
#    La entrada consiste de varios casos de prueba. Cada caso de prueba consiste de varias líneas.La primera línea de un caso de prueba consiste en el tamaño mas pequeño. La segunda línea consiste en el tamaño más grande que puede tener.La tercera linea tiene el número de peces en el acuario.La cuarta línea tiene los tamaños de los peces del acuario separados por un espacio.La entrada termina cuando no hay más datos
#   Salida
#    Por cada caso de prueba escriba en una línea el número de tamaños que puede hallar y que no causen conflictos entre peces.
#   Ejemplo Entrada
#    112111361311000310 100 500588061 3 9 27 243 72929996941 797 120 45 7 120
#   Ejemplo Salida
#    310412110
