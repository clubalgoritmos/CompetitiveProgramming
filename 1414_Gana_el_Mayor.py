#  Descripción
#    Juan y Pedro están jugando cartas. El juego consiste en lo siguiente:1. Se mezclan las 52 cartas que vienen enumeradas del 1 al 52.2. Juan y Pedro retiran cada uno 4 cartas.3. Ambos ponen en la mesa su carta más alta.4. El que tiene la carta más pequeña saca la carta que esta encima del maso de cartas.5. Se repite todo desde el punto 3 hasta que alguno se quede sin cartas.6. El jugador que primero se quede sin cartas gana el juego.Dadas las cartas en el orden que se mezclaron, su programa debe determinar cual de los dos ganó.
#   Entrada
#    La entrada consiste de múltiples casos de prueba.Cada caso de prueba viene en una linea con 52 números separados por un espacio. Esto números del 1 al 52 representan los valores de las cartas.No hay números repetidos.La entrada termina cuando no hay más casos.
#   Salida
#    Escriba en una linea Gana Pedro o Gana Juan según corresponda.
#   Ejemplo Entrada
#    6 13 42 50 19 48 51 14 38 20 36 11 16 31 15 10 37 18 8 30 26 47 27 44 28 35 7 52 41 9 5 22 12 40 43 4 1 29 34 3 45 32 33 49 21 24 46 39 2 25 17 23
#   Ejemplo Salida
