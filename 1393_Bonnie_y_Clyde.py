#    Treeland es un pais de N ciudades conectadas por N-1 carreteras de tal manera que existe exactamente un camino entre cada par de ciudades. Los famosos Bonnie y Clyde planean dar el golpe más grande de la hitoria asaltando Treeland, o por lo menos la mayor parte del país. Llevar el caos a un país entero no es tan sencillo, ya que usualmente robar bancos y matar gente llama la atención de los policias y los pone alerta. Por lo que Bonnie (la estretega) decidió que despues de dar un golpe en alguna de las ciudades en treeland dejar intactas las ciudades que tienen una carretera que las une directamente.
#    Bonnie es inteligente, pero no tanto, por lo que pide tu ayuda para que le digas cuál es la mayor cantidad de ciudades que pueden asaltar con las restricciones ya mencionadas.
#   Entrada
#    La entrada consiste de un sólo caso de prueba. La primera línea de entrada contiene un entero N, que representa la cantidad de cuidades en Treeland. Las siguientes N-1 líneas tienen dos enteros U y V indicando que existe una carretera que conecta directamente la ciudad número U con la ciudad número V. Se garantiza que existe un camino entre cada par de ciudades en Treeland.
#    1<=N<=1000001<=U,V<=N
#   Salida
#    Imprimir un número, la mayor cantidad posible de ciudades que Bonnie y Clyde pueden asaltar.
#   Ejemplo Entrada
#    109 19 29 31 410 59 63 77 83 10
#   Ejemplo Salida
#    6
