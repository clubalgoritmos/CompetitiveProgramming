#  Descripción
#    Tienes n plantas (de area despreciable) que son representados como puntos en el plano. Quieres construir una cerca que encierre a todas las plantas la cual debe tener todos sus lados peralelos al eje x o al eje y. ¿Cuál es el perímetro mínimo que puede tener este polígono si puedes tirar una de las plantas (la que te sea conveniente)?Los puntos no necesariamente deben estar estrictamente dentro del polígono.
#   Entrada
#    La entrada empieza con un número 1<n<=100, que representa la cantidad de plantas que tienes. Las siguientes n líneas tienen un par de números enteros 0<=x,y<=100, que representan la locación de cada una de las plantas en el plano.
#   Salida
#    Imprimir la menor cantidad de cerca que necesitas para encerrar las n-1 plantas restantes. Si sólo se tiene un planta restante o si todas la plantas tienen el mismo valor en x o en y no es necesaria una cerca así que la respuesta en esos casos es igual a 0.
