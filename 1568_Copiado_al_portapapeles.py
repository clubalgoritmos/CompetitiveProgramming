#  Suma de Primos
#  Enviar
#  Estado
#  Descripción
#    Algunos números positivos pueden ser representados por la suma de uno o más números primos consecutivos.¿Cúantas de esta representaciones existen? Por ejemplo el número 53 tiene dos representaciones 5 + 7 + 11 + 13 + 17 y 53. El entero 41 tiene tres representaciones 2 + 3 + 5 + 7 + 11 + 13, 11 + 13 + 17, y 41.El numero 20 no tiene ninguna representación, dado que 7+13 no son numeros primos consecutivos y 3+5+5+7 no es una representación valida dado que el numero 5 esta repetido.Su tarea es hallar en forma eficiente el numero de representaciones que tiene un numero.
#   Entrada
#    La entrada conste de múltiples casos de prueba. Cada caso de prueba viene en una linea y termina cuando no hay más datos. Cada caso de prueba consiste de un numero entero positivo entre 2 y 100000.
#   Salida
#    Por cada caso de prueba escriba una línea con el numero de representaciones que tiene ese numero.
#   Ejemplo Entrada
#    534120177
#   Ejemplo Salida
#    23021
