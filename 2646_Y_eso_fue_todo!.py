#    Taroh es un chico al que le gusta explorar lugares y viajar. En una ocasión, tuvo que ir a un lugar muy, muy lejano. Dado que la hora de llegada sería de más de 10 horas, estaba intentando matar el tiempo. Se puso a jugar con su celular, a leer cómics, a intentar dormir, pero cuando ya no pudo hacer nada de lo mencionado anteriormente debido al cansancio que le provocaron esas actividades durante el viaje, se propuso pensar en el manejo de las cadenas. Tenía en mente el nombre de $n$ personas por sus viajes previos, así que se preguntó: ¿si toma algunas letras de las $n$ personas y las reordena para formar una palabra $s$, cuál sería la palabra $s$ que sea palíndrome(Una palabra que se lee de la misma manera hacia adelante que hacia atrás) y tenga la máxima longitud posible? Como este reto le proporcionaría horas de distracción mientras llegaba a su destino, se propuso cumplirlo. ¡Ayúdale con su cometido! ¡Como es posible este sucesoo!
#   Entrada
#    La entrada consistirá en $T$ casos de prueba $(1 \leq T \leq 1000)$.
#    Cada caso comienza con un número $N$ $(1 \leq N \leq 100)$ que indica la cantidad de cadenas a procesar.
#    A continuación, en la siguiente línea, se proporcionan $N$ cadenas separadas entre sí por un espacio, cada una representada por $a_i$ $(1 \leq |a_i| \leq 20)$, donde las cadenas solo contienen letras minúsculas.
#   Salida
#    Para cada caso mostrar la máxima longitud posible que puede tener la cadena $s$.
#   Ejemplo Entrada
#    45grover paolo rosario kevin joseph6indrack jorge wen fernando diana jhona4miguel alex sergio andy6jesus omar ivan nestor andres gustavo
#   Ejemplo Salida
#    23231123
#   Ayuda
#    Para el 3er caso de tantas posibles respuestas con longitud maxima podemos crear la cadena “egilaealige” de longitud 11.

