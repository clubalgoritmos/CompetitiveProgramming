#    Un matematico muy conocido llamado Taniyama ha estado observando que existen familias de números.
#    Una familia de números es un conjunto en el cual el máximo común divisor de todos en el grupo es un número distinto de 1.
#    Tu tarea es que dado un conjunto de N números en general, se te daran rangos de los cuales debes decir si son o no una familia de números. Para los rangos se empieza a numerar de 0 a N-1. Por ejemplo:
#    Si el conjunto de N=6 que es:
#    2 4 8 16 5 3
#    Por ejemplo el rango de 0 a 3 que serian los numeros 2 4 8 16 es una familia de numeros, mientras que el rango 2 a 5 que son los numeros 8 16 5 3 NO es una familia de numeros.
#   Entrada
#    La primera linea de entrada contendra un número T (1<=T<=100) que indica los casos de prueba del problema. Luego sigue un número N (1<=N<=1000000)el cual indica el tamaño del conjunto en general, luego siguen N números positivos menores a 10^9. Luego un número M que indica los rangos de los cuales se te preguntara. Luego siguen M lineas con dos números A y B (0<=A<B<N).
#   Salida
#    Para cada rango consultado A,B, se debe imprimir "ES UNA FAMILIA", sinsi es una familia de números y "NO ES UNA FAMILIA" sin comillas en caso contrario.
