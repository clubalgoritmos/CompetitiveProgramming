#  Estado
#  Descripción
#    uan ha decido estudiar las frecuencias de aparición de dos letras en un texto. Para esto disponeuna cantidad de texto. Para entender el problema realicemos un ejemplo corto . Su pongamosque el texto es casa cosa amor roma saca en este texto podemos los siguientes pares de letrasca, as, sa, co, os, sa, am, mo, or, ro, om, ma, sa, ac, ca. Analizando las frecuencias de cadapar de letras es: ca = 2, sa = 3 el resto tiene una sola aparición.
#   Entrada
#    Hay varios casos de prueba, cada uno en una línea. Cada línea es una cadena de longitud arbitraria. Y termina cuando no hay más datos en la entrada.
#   Salida
#    La salida consiste del par de letras y la frecuencia separados por un espacio. Solo se listaránlos pares de letras que tengan una frecuencia mayor a $3$. La salida estará ordenada en ordenalfabético, primero por la primera letra luego por la segunda. Después de cada caso de prueba imprima $----------$.
#   Ejemplo Entrada
#    casa cosa amor roma saca acacocacola cocacola todoas a la colacoco coco coco coco toma cocacola
#   Ejemplo Salida
#    ----------co 5la 4----------co 10oc 5----------
#   Ayuda
#    utilizar metodos de cadenas para una mejor busqueda

