#  Descripción
#    Ted se encuentra jugando con las cartas viejas que le dejó su abuelo. El nota que el mazo de cartas de su abuelo es algo raro, pues tiene cartas numeradas de 1 a n.
#    El decide inventar un juego colocara todas las cartas en una fila en orden creciente, es decir que primero colocara el 1, luego el 2 y asi sucesivamente hasta acabar con todas las cartas del azo. En cada ronda del juego él eliminará la primera carta y la que sigue la pondra en el final. Realizará este proceso mientras queden mas de 2 cartas. En la última ronda eliminará la carta que se encuentra al inicio y solo quedará una carta.
#    Tu tarea es ayudar a determinar cual es el número de la carta que queda al final.
#   Entrada
#    La entrada contiene varias casos de prueba hasta fin de archivo. Cada linea de entrada contiene un número entero positivo n (3 <= n <= 50) que representa el número de cartas del mazo del abuelo de Ted.
#   Salida
#    Por cada línea de entrada debes imprimir el número de la carta que cada al final del juego.
#   Ejemplo Entrada
#    6103
#   Ejemplo Salida
#    442
