#    Copiado al portapapeles
#  Ivan y la tabla de multiplicar
#  Enviar
#  Estado
#  Descripción
#    Ivan el campeón no es solo campeón, también es muy inteligente.
#    Mientras que algunos de nosotros estabamos aprendiendo la tabla de multiplicación, Ivan, el campeón se divirtió a su manera. Ivan el campeón pintó una tabla de multiplicación $n \ast m$, donde el elemento en la intersección de la fila $i$-ésima y la columna $j$-ésima equivale a $i \ast j$ (las filas y columnas de la tabla están numeradas a partir de $1$). Luego, se le preguntó: ¿Qué número en la tabla es el $k$-ésimo mas grande más grande? Ivan el campeón siempre respondió correctamente e inmediatamente. ¿Puedes repetir su éxito?
#    Considere la tabla de multiplicación dada. Si escribe todos los números $n \ast m$ de la tabla en el orden no decreciente, entonces el número $k$-ésimo que escribe se llama el $k$-ésimo número más grande de la tabla.
#   Entrada
#    La única línea contiene enteros $n$, $m$ y $k$ ($1 \leq n, m \leq 5 \cdot 10^{5}; 1 \leq k \leq n \ast m$).
#   Salida
#    Imprima el $k$-ésimo número más grande en una tabla de multiplicación $n \ast m$.
#   Ejemplo Entrada
#    2 2 2
#   Ejemplo Salida
#    2
#   Ayuda
#    Una tabla de multiplicar de $2 \ast 3$ se ve así:$1$ $2$ $3$$2$ $4$ $6$

