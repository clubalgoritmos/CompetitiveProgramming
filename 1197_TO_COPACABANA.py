#  Descripción
#    Alguna vez fuiste a Copacabana ? , este lugar tiene una pequeña particularidad, solo se puede llegar cruzando un lago para tal propósito, unos botes se encargan de realizar el trabajo. Los dueños de estos botes son un poco avariciosos así que han pedido su ayuda para que usted les ayude a optimizar sus servicios y por ende ganar más dinero.
#    Usted aceptó al trabajo, con la condición de que, el servicio sea más dirigido en favor de las personas. Usted a oído que los balseros trabajan de la siguiente manera: se paran muy temprano a un lado de la orilla, siempre es la orilla izquierda y esperan a que llegue la primera persona, a cualquier lado de la orilla para correr en su ayuda y transportarla al otro lado del rio, obviamente estos botes tienen una capacidad y estos no pueden sobrepasar esta capacidad.
#   Entrada
#    La entrada consiste de un numero n que indica el número de casos de prueba , por cada caso de prueba se tiene un numero k, t, m donde: k representa la capacidad del bote para llevar personas, t es el tiempo que tarde el bote en cruzar de un lado al otro y m que es el número de personas que llagan a Copacabana. Luego se tienen m líneas que indican la hora de llegada de las personas y si llegan al lado izquierdo y derecho del lago.
#   Salida
#    Por cada caso de entrada, como usted quiere que los visitantes tengan un buen servicio, debe indicar a qué hora llegan los visitantes al otro lado de la orilla. Asuma que el tiempo inicial es 0, imprima una línea en blanco por cada caso de prueba.
#   Ejemplo Entrada
#    22 10 100 left10 left20 left30 left40 left50 left60 left70 left80 left90 left2 10 310 right25 left40 left
#   Ejemplo Salida
#    103030505070709090110304060
