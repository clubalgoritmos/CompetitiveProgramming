#  Descripción
#    En un campo rectangular de tamaño n por m cuadrados hay un ratón y dos gatos. El ratón es el primero en hacer un movimiento, luego cada uno de los gatos hace un movimiento, luego otra vez es el turno del ratón, y así sucesivamente. En cada movimiento, tanto el ratón como los gatos pueden moverse exactamente un cuadrado vertical u horizontalmente. Si el ratón está de pie en el borde del campo, en su siguiente movimiento puede saltar del campo y se salva de los gatos. Si en el siguiente movimiento uno de los gatos se mueve al campo con el ratón entonces no hay escape para el ratón ... =(Debe escribir un programa que, conociendo las posiciones iniciales del ratón y de los dos gatos, averigüe si hay alguna forma de que el ratón escape de los gatos, asumiendo, por supuesto, que cada gato hará lo posible por atrapar al ratón.
#   Entrada
#    En la primera línea de entrada se dan dos números enteros n y m, que no exceden de 100, donde n es el número de filas, y m - el número de columnas. La segunda línea contiene un número k [k <= 10], que define el número de casos de prueba para el campo dado. En las siguientes líneas k se dan las posiciones iniciales del ratón y los gatos. La posición en el campo viene dada por dos números: el primero es el número de la fila, el segundo es el número de la columna. Los dos primeros números enteros son las coordenadas del ratón, los siguientes cuatro números enteros son las coordenadas de los gatos.
#   Salida
#    Debes emitir líneas k con respuestas para cada caso de prueba. La respuesta es "YES", si el ratón puede escapar o "NO" de otra manera.
#   Ejemplo Entrada
#    5 332 2 1 1 3 32 3 1 3 5 23 2 1 2 4 3
#   Ejemplo Salida
#    NOYESYES
