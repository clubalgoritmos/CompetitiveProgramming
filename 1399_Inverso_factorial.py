#  Descripción
#    Se define al factorial de un número $N$, como el producto de todos los números entre 1 a $N$: N! = 1 * 2 * 3 * 4 * ... * N|con $0! = 1$.Por ejemplo 5! = 1 * 2 * 3 * 4 * 5 = 120Lo que se pide en este problema, como el título dice, es calcular el inverso del factorial, en otras palabras encontrar un número tal que aplicando el factorial es igual al número de la entrada.Por ejemplo: el inverso de 120 es 5, ya que $5! = 120$, el inverso de 6 es 3 ya que $3! = 6$
#   Entrada
#    La primera linea indica el numero de casos de prueba.
#    Consta de varios números que tienen entre 1 y $10^6$ dígitos, estos son  factoriales válidos.  Los números son mayores a 1.
#   Salida
#    Para cada número que recibas en la entrada le corresponde una línea en la salida, que es el cálculo del inverso del factorial.
#   Ejemplo Entrada
#    21206
#   Ejemplo Salida
#    53
